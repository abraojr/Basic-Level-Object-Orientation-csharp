using System;

namespace Keyword_Var
{
    class Program
    {
        static void Main(string[] args)
        {
            var x = 10;
            var y = 20.0;
            var z = "Maria";

            Console.WriteLine(x); //OUTPUT: 10
            Console.WriteLine(y); //OUTPUT: 20
            Console.WriteLine(z); //OUTPUT: Maria

        }
    }
}
